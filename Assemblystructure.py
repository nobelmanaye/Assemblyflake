class Assemstruct(object):
    def __init__(self):
        self.count = 0
        self.lst = []
        self.dct = {}
    def segmentadd(linelst):
        for word in linelst:
            self.lst.append(word.split())
            self.lst.append(word)
        self.dct.append(self.lst)

        self.lst.clear()

   
